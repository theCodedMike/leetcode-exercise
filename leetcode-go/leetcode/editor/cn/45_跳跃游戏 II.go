func reverseTraversal(nums []int) int {
	pos := len(nums) - 1
	steps := 0

	for pos != 0 {
		for i := 0; i < pos; i++ {
			if i + nums[i] >= pos {
				pos = i
				steps++
				break
			}
		}
	}

	return steps
}

func greedy(nums []int) int {
	maxPos, size, end := 0, len(nums), 0
	steps := 0

	for i := 0; i < size - 1; i++ {
		if maxPos >= i {
			maxPos = max(maxPos, i + nums[i])
			if i == end {
				end = maxPos
				steps++
			}
		}
	}

	return steps
}
